int		ft_iterative_factorial(int nb)
{
	int		c;
	int		fact;

	c = 1;
	fact = 1;
	if (nb > 12 || nb < 0)
	{
		return (0);
	}
	else
	{
		while (c <= nb)
		{
			fact = fact * c;
			c++;
		}
		return (fact);
	}
}
