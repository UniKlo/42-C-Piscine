char	*ft_strrev(char *str)
{
	char	temp_c;
	int		len;
	int		n;

	len = 0;
	n = 0;
	while (str[len] != '\0')
	{
		len++;
	}
	while (n < len / 2)
	{
		temp_c = str[n];
		str[n] = str[len - n - 1];
		str[len - n - 1] = temp_c;
		n++;
	}
	return (str);
}
